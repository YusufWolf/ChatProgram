    private void sendMessage(String messageContent) {
        if (isUser == 0) { // group message
            Message msg = new Message(Message.Message_Type.SendGroupName);
            msg.content = lbl_chat.getText();
            Client.Send(msg);
            Message msg2 = new Message(Message.Message_Type.SendGroupMessage);
            msg2.content = messageContent;
            Client.Send(msg2);
        } else { // user message
            Message msg = new Message(Message.Message_Type.SendUserName);
            msg.content = lbl_chat.getText();
            Client.Send(msg);
            String fullMsg = ChatApp.myApp.txtb_userName.getText() + ": " + messageContent + "\n";
            int result = ChatApp.myApp.checkUserList(lbl_chat.getText());
            if (result == -1) {
                ChatApp.myApp.userArray.add(lbl_chat.getText());
                ChatApp.myApp.userMessageArray.add(fullMsg);
                txta_chat.setText(fullMsg);
            } else {
                ChatApp.myApp.userMessageArray.set(result, (ChatApp.myApp.userMessageArray.get(result) + fullMsg));
                txta_chat.setText(ChatApp.myApp.userMessageArray.get(result));
            }
            Message msg2 = new Message(Message.Message_Type.SendUserMessage);
            msg2.content = fullMsg;
            Client.Send(msg2);
        }
        txtb_message.setText("");
    }
